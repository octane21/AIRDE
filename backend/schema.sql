-- AIRDE Database Schema

-- Users table
CREATE TABLE IF NOT EXISTS users (
  id SERIAL PRIMARY KEY,
  username VARCHAR(50) UNIQUE NOT NULL,
  email VARCHAR(100) UNIQUE NOT NULL,
  password_hash VARCHAR(255) NOT NULL,
  role VARCHAR(20) NOT NULL CHECK (role IN ('admin', 'operator')),
  is_active BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Assets table (asset register) — sheet 03_Asset_Register
CREATE TABLE IF NOT EXISTS assets (
  id VARCHAR(20) PRIMARY KEY,
  line VARCHAR(100),
  segment VARCHAR(50),
  location VARCHAR(50),
  service VARCHAR(50),
  nps DECIMAL(10,2),
  od DECIMAL(10,2),
  material VARCHAR(100),
  install_year INT,
  t_design DECIMAL(10,3),
  op_pressure DECIMAL(10,2),
  des_pressure DECIMAL(10,2),
  ca DECIMAL(10,2),
  dft_target DECIMAL(10,2),
  criticality VARCHAR(20),
  status VARCHAR(20) DEFAULT 'Active',
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Criticality assessment — sheet 05_Criticality
CREATE TABLE IF NOT EXISTS criticality (
  asset_id VARCHAR(20) PRIMARY KEY REFERENCES assets(id) ON DELETE CASCADE,
  safety INT,
  environment INT,
  operation INT,
  financial INT,
  criticality_score INT GENERATED ALWAYS AS (COALESCE(safety,0)+COALESCE(environment,0)+COALESCE(operation,0)+COALESCE(financial,0)) STORED,
  criticality VARCHAR(20),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Inspection records — sheet 06_Inspection
CREATE TABLE IF NOT EXISTS inspections (
  id SERIAL PRIMARY KEY,
  inspection_code VARCHAR(30) UNIQUE,
  asset_id VARCHAR(20) REFERENCES assets(id) ON DELETE CASCADE,
  inspection_date DATE,
  inspector VARCHAR(100),
  method VARCHAR(100),
  kp_location VARCHAR(50),
  status VARCHAR(30),
  remarks TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- DFT (coating thickness) readings — sheet 07_DFT
CREATE TABLE IF NOT EXISTS dft_readings (
  id SERIAL PRIMARY KEY,
  asset_id VARCHAR(20) REFERENCES assets(id) ON DELETE CASCADE,
  reading_date DATE,
  kp_location VARCHAR(50),
  dft_target DECIMAL(10,2),
  dft_actual DECIMAL(10,2),
  coating_health DECIMAL(10,4),
  coating_status VARCHAR(20),
  coating_type VARCHAR(50),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (asset_id, reading_date, kp_location)
);

-- UT (ultrasonic thickness) readings — sheet 08_UT
CREATE TABLE IF NOT EXISTS ut_readings (
  id SERIAL PRIMARY KEY,
  asset_id VARCHAR(20) REFERENCES assets(id) ON DELETE CASCADE,
  reading_date DATE,
  kp_location VARCHAR(50),
  t_design DECIMAL(10,3),
  deg0 DECIMAL(10,3),
  deg90 DECIMAL(10,3),
  deg180 DECIMAL(10,3),
  deg270 DECIMAL(10,3),
  t_actual_min DECIMAL(10,3),
  tmin_required DECIMAL(10,3),
  thickness_health DECIMAL(10,4),
  corrosion_rate DECIMAL(10,4),
  remaining_life DECIMAL(10,2),
  ut_status VARCHAR(20),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (asset_id, reading_date, kp_location)
);

-- Visual inspection findings — sheet 09_Visual
CREATE TABLE IF NOT EXISTS visual_inspections (
  id SERIAL PRIMARY KEY,
  asset_id VARCHAR(20) REFERENCES assets(id) ON DELETE CASCADE,
  inspection_date DATE,
  kp_location VARCHAR(50),
  finding VARCHAR(100),
  severity VARCHAR(20),
  visual_score DECIMAL(10,2),
  leakage VARCHAR(50),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (asset_id, inspection_date, kp_location)
);

-- Photo records — sheet 10_Photo
CREATE TABLE IF NOT EXISTS photos (
  id SERIAL PRIMARY KEY,
  asset_id VARCHAR(20) REFERENCES assets(id) ON DELETE CASCADE,
  kp_location VARCHAR(50),
  photo_file VARCHAR(255),
  original_name VARCHAR(255),
  photo_type VARCHAR(50),
  geo_tagged BOOLEAN DEFAULT FALSE,
  finding VARCHAR(100),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (asset_id, kp_location, photo_file)
);

-- Findings register — sheet 12_Findings
CREATE TABLE IF NOT EXISTS findings (
  finding_code VARCHAR(20) PRIMARY KEY,
  asset_id VARCHAR(20) REFERENCES assets(id) ON DELETE CASCADE,
  finding_date DATE,
  kp_location VARCHAR(50),
  finding VARCHAR(150),
  severity VARCHAR(20),
  status VARCHAR(30),
  evidence VARCHAR(150),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Risk data table (engine-managed) — sheet 13_Risk_Intelligence
CREATE TABLE IF NOT EXISTS risk_data (
  id VARCHAR(20) PRIMARY KEY REFERENCES assets(id) ON DELETE CASCADE,
  ahi DECIMAL(10,2),
  condition VARCHAR(20),
  coating_health DECIMAL(10,2),
  thickness_health DECIMAL(10,2),
  corrosion_rate DECIMAL(10,4),
  tmin DECIMAL(10,3),
  remaining_life DECIMAL(10,1),
  visual_score DECIMAL(10,2),
  pof DECIMAL(10,4),
  cof DECIMAL(10,2),
  risk_score DECIMAL(10,4),
  risk_level VARCHAR(20),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);
ALTER TABLE risk_data ADD COLUMN IF NOT EXISTS visual_score DECIMAL(10,2);

-- Action register (engine-managed) — sheet 14_Action_Register
CREATE TABLE IF NOT EXISTS action_register (
  id VARCHAR(20) PRIMARY KEY REFERENCES assets(id) ON DELETE CASCADE,
  strategy VARCHAR(50),
  action TEXT,
  priority VARCHAR(10),
  due_date DATE,
  status VARCHAR(30),
  risk_level VARCHAR(20),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Maintenance action plan — sheet 19_Maintenance_Action_Plan
CREATE TABLE IF NOT EXISTS maintenance_action_plan (
  id SERIAL PRIMARY KEY,
  asset_id VARCHAR(20) UNIQUE REFERENCES assets(id) ON DELETE CASCADE,
  priority VARCHAR(10),
  action TEXT,
  owner VARCHAR(100),
  target_date DATE,
  status VARCHAR(30),
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Work orders — sheet 20_Work_Order
CREATE TABLE IF NOT EXISTS work_orders (
  wo_no VARCHAR(20) PRIMARY KEY,
  asset_id VARCHAR(20) UNIQUE REFERENCES assets(id) ON DELETE CASCADE,
  work_type VARCHAR(50),
  description TEXT,
  priority VARCHAR(10),
  due_date DATE,
  status VARCHAR(30),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Maintenance strategy reference — sheet 18_Maintenance_Strategy
CREATE TABLE IF NOT EXISTS maintenance_strategy (
  strategy VARCHAR(50) PRIMARY KEY,
  definition TEXT,
  typical_condition VARCHAR(100),
  example_actions TEXT,
  priority VARCHAR(10),
  due_days INT
);

-- Inspection teams — single source of truth for the "Inspector" dropdown on Inspection (06_Inspection).
CREATE TABLE IF NOT EXISTS teams (
  name VARCHAR(50) PRIMARY KEY,
  lead VARCHAR(100),
  member_count INT,
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Engine config (AHI weights etc.) — sheet 99_Config
CREATE TABLE IF NOT EXISTS config (
  param VARCHAR(50) PRIMARY KEY,
  value DECIMAL(10,4),
  notes VARCHAR(150)
);

-- Trend snapshots (history for charts) — sheet 16_Trend
CREATE TABLE IF NOT EXISTS trend_snapshots (
  id SERIAL PRIMARY KEY,
  snapshot_date DATE NOT NULL UNIQUE,
  avg_corrosion_rate DECIMAL(10,4),
  avg_ahi DECIMAL(10,2),
  high_extreme_count INT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Report sections — sheet 21_Report
CREATE TABLE IF NOT EXISTS report_sections (
  section VARCHAR(50) PRIMARY KEY,
  summary TEXT,
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Lookup reference — sheet 98_Lookup
-- min_value/max_value drive the engine's actual categorization (Health Status, Risk Level,
-- Coating Status bands) — editing them here changes how AHI/Risk/Coating are categorized.
CREATE TABLE IF NOT EXISTS lookup_reference (
  id SERIAL PRIMARY KEY,
  category VARCHAR(50),
  value VARCHAR(50),
  description VARCHAR(150),
  min_value DECIMAL(10,4),
  max_value DECIMAL(10,4)
);

-- Formula map reference — sheet 22_Formula_Map
CREATE TABLE IF NOT EXISTS formula_map (
  id SERIAL PRIMARY KEY,
  engine VARCHAR(50),
  sheet VARCHAR(50),
  formula TEXT,
  purpose VARCHAR(100),
  feeds_to VARCHAR(100)
);

